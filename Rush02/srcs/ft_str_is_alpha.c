int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] > 64 && str[i] < 123)
			|| (str[i] > 90 && str[i] < 97))
			i++;
		return (0);
	}
	return (1);
}
